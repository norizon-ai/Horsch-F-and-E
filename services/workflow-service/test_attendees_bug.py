#!/usr/bin/env python3
"""
Test script to reproduce and verify the attendees bug fix.

Bug: When users change speaker names in verification step (e.g., "Speaker 0" → "Omar"),
the names DO NOT appear in the generated protocol's attendees field.
"""

import sys
import logging
from app.models import Speaker, Protocol, ActionItem, UpdateSpeakersRequest
from app.services.mock_generator import MockGenerator
from app.routers.protocol import _apply_speaker_name_replacements
from app.services.job_manager import JobManager

# Enable detailed logging
logging.basicConfig(
    level=logging.INFO,
    format='%(levelname)s - %(name)s - %(message)s'
)
logger = logging.getLogger(__name__)

def print_separator(title=""):
    print("\n" + "=" * 80)
    if title:
        print(f"  {title}")
        print("=" * 80)

def test_scenario_1_empty_confirmed_names():
    """
    SCENARIO 1: Frontend sends speakers with EMPTY confirmedName
    This simulates the bug where frontend doesn't properly send confirmed names.
    """
    print_separator("SCENARIO 1: Empty confirmedName (Bug Simulation)")

    print("\n1. Initial speakers generated by transcription:")
    speakers = [
        Speaker(
            id="speaker-1",
            detectedName="Speaker 0",
            confirmedName=""  # Empty - simulating bug
        ),
        Speaker(
            id="speaker-2",
            detectedName="Speaker 1",
            confirmedName=""
        )
    ]

    for i, s in enumerate(speakers):
        print(f"  Speaker {i}: detected='{s.detected_name}', confirmed='{s.confirmed_name}'")

    print("\n2. Generate protocol with these speakers:")
    mock_gen = MockGenerator()
    protocol = mock_gen.generate_protocol(speakers=speakers)

    print(f"  Attendees: {protocol.attendees}")
    print(f"  Expected: ['Speaker 0', 'Speaker 1'] (since confirmedName is empty)")

    print("\n3. Apply speaker name replacements:")
    result = _apply_speaker_name_replacements(protocol, speakers)

    print(f"  Attendees after replacement: {result.attendees}")
    print(f"  Expected: ['Speaker 0', 'Speaker 1'] (no replacement possible with empty confirmedName)")

    # Verify
    if result.attendees == ['Speaker 0', 'Speaker 1']:
        print("\n✓ PASS: Attendees correctly show detected names when confirmedName is empty")
        return True
    else:
        print(f"\n✗ FAIL: Unexpected attendees: {result.attendees}")
        return False


